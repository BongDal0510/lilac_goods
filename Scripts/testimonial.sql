CREATE SEQUENCE SEQ_TESTIMONIAL;

CREATE TABLE TBL_TESTIMONIAL(
	TESTIMONIAL_ID NUMBER CONSTRAINT PK_TESTIMONIAL PRIMARY KEY,
	TESTIMONIAL_TITLE VARCHAR2(500) NOT NULL,
	TESTIMONIAL_CONTENT VARCHAR2(1000) NOT NULL,
	TESTIMONIAL_REGISTER_DATE DATE DEFAULT SYSDATE,
	TESTIMONIAL_UPDATE_DATE DATE DEFAULT SYSDATE,
	TESTIMONIAL_USER_ID NUMBER,
	CONSTRAINT FK_USER_ID FOREIGN KEY (TESTIMONIAL_USER_ID) REFERENCES TBL_USER (USER_ID)
);


CREATE SEQUENCE SEQ_USER;

CREATE TABLE TBL_USER(
	USER_NO NUMBER CONSTRAINT PK_USER PRIMARY KEY,
	USER_ID VARCHAR2(1000) CONSTRAINT UK_USER_ID UNIQUE NOT NULL,
	USER_PW VARCHAR2(1000) NOT NULL,
	USER_NAME VARCHAR2(1000) NOT NULL,
	USER_PHONENUMBER VARCHAR2(1000) NOT NULL,	
	USER_AGE NUMBER(3) NOT NULL,
	USER_GENDER VARCHAR2(1000) NOT NULL,
	USER_EMAIL VARCHAR2(500),
	USER_ZIPCODE VARCHAR2(500),
	USER_ADDRESS VARCHAR2(1000),
	USER_ADDRESS_DETAIL VARCHAR2(1000),
	USER_ADDRESS_ETC VARCHAR2(1000)
);

